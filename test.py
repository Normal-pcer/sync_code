s="""                ********
               ************
               ####....#.
             #..###.....##....
             ###.......######              ###            ###
                ...........               #...#          #...#
               ##*#######                 #.#.#          #.#.#
            ####*******######             #.#.#          #.#.#
           ...#***.****.*###....          #...#          #...#
           ....**********##.....           ###            ###
           ....****    *****....
             ####        ####
           ######        ######
##############################################################
#...#......#.##...#......#.##...#......#.##------------------#
###########################################------------------#
#..#....#....##..#....#....##..#....#....#####################
##########################################    #----------#
#.....#......##.....#......##.....#......#    #----------#
##########################################    #----------#
#.#..#....#..##.#..#....#..##.#..#....#..#    #----------#
##########################################    ############  """
prev, prev_i = s[0], 0
for i in range(1, len(s)):
    if prev != s[i]:
        k = "*#-. \n"
        if not s[i] in k:
            print(ord(s[i]))
        l = i - prev_i
        if prev == '\n':
            print(chr(90+33),end='')
        else:
            idx = k.index(prev)
            a = (l-1) // 18
            b = (l-1) % 18
            for j in range(a):
                print(chr(17 + 18 * idx + 33), end='')
            print(chr(b + 18 * idx + 33), end='')
        prev, prev_i = s[i], i